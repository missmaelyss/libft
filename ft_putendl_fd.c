#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	int n;

	n = 0;
	while (s[n])
	{
		ft_putchar_fd(s[n], fd);
		n++;
	}
	ft_putchar_fd('\n', fd);
}
