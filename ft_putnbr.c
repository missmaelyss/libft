#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == 0)
		ft_putchar('0');
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n != 0 && n != -2147483648)
	{
		if (n / 10 > 0)
			ft_putnbr(n / 10);
		ft_putchar(48 + n % 10);
	}
}
